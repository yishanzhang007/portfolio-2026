import { chromium } from "playwright";
import { promises as fs } from "fs";
import path from "path";

const DIR = "public/work/clinic-ai-assistant";
const files = (await fs.readdir(DIR)).filter((f) => f.endsWith("@3x.png"));

const browser = await chromium.launch();
const blurMap = {};

for (const file of files) {
  const inputPath = path.resolve(DIR, file);
  const buf = await fs.readFile(inputPath);
  const dataUri = `data:image/png;base64,${buf.toString("base64")}`;
  // Tiny canvas — 16×12 stretched preview is plenty for a blur background.
  const ctx = await browser.newContext({ viewport: { width: 16, height: 12 } });
  const page = await ctx.newPage();
  await page.setContent(
    `<html><body style="margin:0;background:#000;"><img src="${dataUri}" style="width:16px;height:12px;display:block"></body></html>`,
  );
  await page.waitForLoadState("networkidle");
  await page.waitForTimeout(50);
  const out = await page.screenshot({ type: "jpeg", quality: 60 });
  const publicPath = `/work/clinic-ai-assistant/${file}`;
  blurMap[publicPath] = `data:image/jpeg;base64,${out.toString("base64")}`;
  await ctx.close();
  console.log(`  ${file}  →  ${out.length} bytes`);
}

await browser.close();
await fs.writeFile(
  "lib/blur-data.ts",
  `// Auto-generated by scripts/gen-blur-data.mjs. Re-run after rasterizing
// new heroes via scripts/svg-to-png.mjs.
//
// Maps the public src path of a hero raster to a tiny base64 JPEG
// thumbnail used as next/image's blur placeholder.
export const blurDataURLs: Record<string, string> = ${JSON.stringify(blurMap, null, 2)};
`,
);
console.log(`Wrote ${Object.keys(blurMap).length} entries to lib/blur-data.ts`);
